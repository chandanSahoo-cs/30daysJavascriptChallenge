function sqaureMinusOne(num1){
    return num1*num1-1;
}

function sqaurePlusOne(num1){
    return num1*num1+1;
}

function cube(num1){
    return num1*num1*num1
}

function cubePlusOne(num1){
    return num1*num1*num1+1
}

const fxn={
    sqaureMinusOne,
    sqaurePlusOne,
    cube,
    cubePlusOne
}

export default fxn